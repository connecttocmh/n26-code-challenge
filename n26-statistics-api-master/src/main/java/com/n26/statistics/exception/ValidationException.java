package com.n26.statistics.exception;

import com.n26.statistics.common.ErrorCodes;

import lombok.AllArgsConstructor;
import lombok.Data;

/**
 * Unchecked exception will be thrown when there is invalid input data is entered by the user.
 *
 * @author Chetankumar Hiremath
 *
 */
@Data
@AllArgsConstructor
public class ValidationException extends RuntimeException {
    /**
     *
     */
    private static final long serialVersionUID = -850763550439325463L;

    // ***************************************************************************************************************
    // ******************************************** Public Fields ****************************************************
    // ***************************************************************************************************************

    // ***************************************************************************************************************
    // ****************************************** Non Public Fields **************************************************
    // ***************************************************************************************************************

    private final Integer errorCode;

    private final String errorMessage;

    // ***************************************************************************************************************
    // *********************************************** Constructors **************************************************
    // ***************************************************************************************************************

    public ValidationException(ErrorCodes code) {
        super(code.code() + "-" + code.message());
        this.errorCode = code.code();
        this.errorMessage = code.message();
    }

    // ***************************************************************************************************************
    // ******************************************** Public Methods ***************************************************
    // ***************************************************************************************************************

    // ***************************************************************************************************************
    // ****************************************** Non Public Methods *************************************************
    // ***************************************************************************************************************
}
