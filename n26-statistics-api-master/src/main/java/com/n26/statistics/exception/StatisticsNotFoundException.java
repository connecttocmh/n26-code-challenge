package com.n26.statistics.exception;

import com.n26.statistics.common.ErrorCodes;

import lombok.Data;

/**
 *
 * Unchecked exception will be thrown when there is no statistics data for last 60 seconds.
 *
 * @author Chetankumar Hiremath
 *
 */
@Data
public class StatisticsNotFoundException extends RuntimeException {
    /**
     *
     */
    private static final long serialVersionUID = -4400138999315443329L;

    // ***************************************************************************************************************
    // ******************************************** Public Fields ****************************************************
    // ***************************************************************************************************************

    // ***************************************************************************************************************
    // ****************************************** Non Public Fields **************************************************
    // ***************************************************************************************************************

    private final Integer errorCode;

    private final String errorMessage;

    // ***************************************************************************************************************
    // *********************************************** Constructors **************************************************
    // ***************************************************************************************************************

    public StatisticsNotFoundException(ErrorCodes code) {
        this.errorCode = code.code();
        this.errorMessage = code.message();
    }

    // ***

    // ***************************************************************************************************************
    // ******************************************** Public Methods ***************************************************
    // ***************************************************************************************************************

    // ***************************************************************************************************************
    // ****************************************** Non Public Methods *************************************************
    // ***************************************************************************************************************
}
